public class Start {
    public static void main(String[] args) {

        System.out.println("for loop");
        for (int x = 0; x <= 5; x++) {
            System.out.println("Value of x = " + x);
        }

        int y = 0;
        System.out.println("\nwhile loop");
        while ( y <= 5) {
            System.out.println("Value of y = " + y);
            y++;
        }
    }
}
